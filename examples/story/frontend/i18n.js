let currentLang = 'zh';

// Initialize language from localStorage if available
try {
  const savedLang = localStorage.getItem('app_lang');
  if (savedLang && (savedLang === 'zh' || savedLang === 'en')) {
    currentLang = savedLang;
  }
} catch (e) {}

function t(key) {
  if (translations[currentLang] && translations[currentLang][key]) {
    return translations[currentLang][key];
  }
  // Return the key itself or the Chinese fallback if not found
  if (translations['zh'] && translations['zh'][key]) {
    return translations['zh'][key];
  }
  return key;
}

function updateI18nDOM() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      if (el.type === 'button' || el.type === 'submit') {
        el.value = t(key);
      }
    } else {
      // Don't replace child nodes if possible, but innerHTML is easiest for simple elements
      // For elements with text and child nodes, data-i18n should be applied to the specific text wrapper
      el.textContent = t(key);
    }
  });

  // Update titles
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.getAttribute('data-i18n-title');
    el.title = t(key);
  });
  
  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    el.placeholder = t(key);
  });
  
  // Update alt attributes
  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.getAttribute('data-i18n-alt');
    el.alt = t(key);
  });

  // Re-render UI components in app.js if they exist
  if (typeof renderAgentList === 'function') renderAgentList();
  if (typeof currentSelectedAgent !== 'undefined' && currentSelectedAgent && typeof renderDetail === 'function') {
    renderDetail(currentSelectedAgent);
  }
  if (typeof renderPresetButtons === 'function') renderPresetButtons();
  if (typeof renderManagePresetList === 'function') renderManagePresetList();
  if (typeof updateTickNavButtons === 'function') updateTickNavButtons();
  if (typeof refreshDialogueSidebar === 'function') refreshDialogueSidebar();
  
  // Also translate time if displayed
  if (typeof currentTick !== 'undefined' && currentTick >= 0 && typeof tickToChineseDate === 'function') {
    const simDateEl = document.getElementById('simDate');
    if (simDateEl) {
      simDateEl.textContent = tickToChineseDate(currentTick);
    }
  }
  
  document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : 'en';
}

function toggleLanguage() {
  currentLang = currentLang === 'zh' ? 'en' : 'zh';
  try {
    localStorage.setItem('app_lang', currentLang);
  } catch (e) {}
  updateI18nDOM();
}

// Run once on load
document.addEventListener('DOMContentLoaded', () => {
  updateI18nDOM();
});
